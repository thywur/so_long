#include "libft.h"

int	ft_printstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (ft_printstr("(null)"));
	while (str[i])
	{
		if (write(1, &str[i++], 1) == -1)
			return (-1);
	}
	return (i);
}
